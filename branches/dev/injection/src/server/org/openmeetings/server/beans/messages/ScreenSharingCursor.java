package org.openmeetings.server.beans.messages;

public class ScreenSharingCursor {
	
	private String s;
	private Integer x;
	private Integer y;
	
	public String getS() {
		return s;
	}
	public void setS(String s) {
		this.s = s;
	}
	
	public Integer getX() {
		return x;
	}
	public void setX(Integer x) {
		this.x = x;
	}
	
	public Integer getY() {
		return y;
	}
	public void setY(Integer y) {
		this.y = y;
	}

}
